#!/bin/bash

# Скрипт сборки операционной системы

# Создание каталога для сборки
mkdir -p ../build

echo "Компиляция загрузчика..."

# Компиляция загрузчика
nasm -f bin ../src/boot/boot.asm -o ../build/boot.bin

# Проверка успешности компиляции
if [ $? -eq 0 ]; then
    echo "Загрузчик успешно скомпилирован: build/boot.bin"
else
    echo "Ошибка компиляции загрузчика!"
    exit 1
fi

# Компиляция ядра
# Для компиляции ядра нам понадобится кросс-компилятор
# Пока используем простой подход с NASM для ассемблерных файлов
# и предполагаем, что у нас есть скрипт для компиляции C файлов

echo "Компиляция ядра..."

# Компиляция ассемблерных файлов ядра
nasm -f elf32 ../src/kernel/kernel.asm -o ../build/kernel.o

# Здесь должен быть код для компиляции C файлов ядра
# Пока оставим заглушку

# Создание простого ядра из ассемблерного файла
# В реальной системе здесь будет линковка всех объектных файлов

# Пока создаем простое ядро из kernel.asm
nasm -f bin ../src/kernel/kernel.asm -o ../build/kernel.bin

# Проверка успешности компиляции ядра
if [ $? -eq 0 ]; then
    echo "Ядро успешно скомпилировано: build/kernel.bin"
else
    echo "Ошибка компиляции ядра!"
    exit 1
fi

# Создание загрузочного образа
echo "Создание загрузочного образа..."

# Создание образа с загрузчиком
# Загрузчик должен помещаться в первые 512 байт
# Ядро будет загружаться сразу после загрузчика

cp ../build/boot.bin ../build/os.img

# Добавление ядра в образ
# В реальной системе здесь будет более сложная логика
# для размещения ядра по определенному адресу

cat ../build/kernel.bin >> ../build/os.img

# Заполнение образа до размера 1.44MB (стандартный размер флоппи-диска)
# Сначала определим текущий размер
CURRENT_SIZE=$(stat -c%s "../build/os.img")
TARGET_SIZE=$((1440 * 1024))
PADDING_SIZE=$((TARGET_SIZE - CURRENT_SIZE))

if [ $PADDING_SIZE -gt 0 ]; then
    dd if=/dev/zero bs=1 count=$PADDING_SIZE 2>/dev/null >> ../build/os.img
fi

if [ $? -eq 0 ]; then
    echo "Загрузочный образ успешно создан: build/os.img"
    echo "Размер образа: $(stat -c%s "../build/os.img") байт"
    echo "Для запуска в QEMU выполните: qemu-system-i386 -fda build/os.img"
else
    echo "Ошибка создания загрузочного образа!"
    exit 1
fi
