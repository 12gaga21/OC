bits 32

section .text
    global fs_write_sector
    global fs_create_file
    global fs_create_dir
    global fs_delete_entry
    extern printf

; Запись сектора на диск
; Вход: eax - номер сектора
;       esi - адрес данных для записи
fs_write_sector:
    pusha
    push es
    
    ; Установка сегмента ES на адрес данных
    mov ax, 0x4000  ; Сегмент для буфера данных
    mov es, ax
    
    ; Копирование данных в буфер (если необходимо)
    ; В данном случае предполагаем, что данные уже в нужном сегменте
    
    ; Запись сектора на диск
    mov ah, 0x03    ; Функция записи секторов
    mov al, 1       ; Количество секторов для записи
    mov ch, 0       ; Номер дорожки (временно 0)
    mov cl, al      ; Номер сектора
    mov dh, 0       ; Номер головки
    mov dl, 0x80    ; Номер диска
    mov bx, 0       ; Смещение в сегменте ES
    int 0x13        ; Вызов BIOS
    
    pop es
    popa
    ret

; Создание нового файла
; Вход: esi - адрес имени файла (8.3 формат, 11 байт)
;       edi - адрес содержимого файла
;       ecx - размер содержимого файла в байтах
fs_create_file:
    pusha
    
    ; Найти свободную запись в текущей директории
    ; (временно упрощенная реализация)
    
    ; Найти свободный кластер для файла
    ; (временно упрощенная реализация)
    
    ; Записать содержимое файла в кластер
    ; (временно упрощенная реализация)
    
    ; Обновить запись в директории
    ; (временно упрощенная реализация)
    
    ; Обновить FAT
    ; (временно упрощенная реализация)
    
    popa
    ret

; Создание новой директории
; Вход: esi - адрес имени директории (8.3 формат, 11 байт)
fs_create_dir:
    pusha
    
    ; Найти свободную запись в текущей директории
    ; (временно упрощенная реализация)
    
    ; Найти свободный кластер для директории
    ; (временно упрощенная реализация)
    
    ; Создать запись "." (текущая директория)
    ; (временно упрощенная реализация)
    
    ; Создать запись ".." (родительская директория)
    ; (временно упрощенная реализация)
    
    ; Обновить запись в родительской директории
    ; (временно упрощенная реализация)
    
    ; Обновить FAT
    ; (временно упрощенная реализация)
    
    popa
    ret

; Удаление файла или директории
; Вход: esi - адрес имени файла/директории (8.3 формат, 11 байт)
fs_delete_entry:
    pusha
    
    ; Найти запись в текущей директории
    ; (временно упрощенная реализация)
    
    ; Пометить запись как удаленную (0xE5)
    ; (временно упрощенная реализация)
    
    ; Освободить кластеры в FAT
    ; (временно упрощенная реализация)
    
    ; Обновить FAT на диске
    ; (временно упрощенная реализация)
    
    ; Обновить директорию на диске
    ; (временно упрощенная реализация)
    
